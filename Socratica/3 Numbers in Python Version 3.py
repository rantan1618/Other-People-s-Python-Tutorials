# Types of numbers in Python Version 3
# int, float, complex
a =  496 # This is a perfect number.
# you can see the data type by using the type function.
print(a)
print(type(a))

# Floats are how decimal values are stored.
e = 2.718281828
print(e)
print(type(e))

# complex numbers
z = 2 - 6.1j
print(type(z))
# To see the real part access the real property
print(z.real)
# access the imaginary part with property imag
print(z.imag)